import mongoose from "mongoose";
import { Request, Response, NextFunction } from "express";

import * as AccountRolesAPITypes from "../../../../shared/types/api/account-roles";
import { IAccount } from "../../../../shared/models/account";

import LoggingService from "../../services/logging";
import {
	AccountRoleNotFoundError,
	restoreAccountRoleWithRetry,
} from "../../services/account-roles/restore";

const handler = async (
	req: Request<{}, {}, AccountRolesAPITypes.RestoreRequestBody>,
	res: Response<AccountRolesAPITypes.RestoreResponseData>,
	_next: NextFunction,
) => {
	const session = await mongoose.startSession();
	const { roleId } = req.body;
	const adminAccount = req.user as IAccount;

	try {
		session.startTransaction();

		const restoredRole = await restoreAccountRoleWithRetry(roleId, {
			session,
			traceId: req.traceId,
			adminAccount,
			terminalId: req.terminalId,
		});

		await session.commitTransaction();

		res.status(200).json({
			status: "success",
			accountRole: restoredRole,
		});
	} catch (error: unknown) {
		await session.abortTransaction();

		if (error instanceof AccountRoleNotFoundError) {
			res.status(404).json({
				status: "role-not-found",
			});
			return;
		}

		if (error instanceof Error) {
			LoggingService.log({
				source: "api:account-roles:restore",
				level: "error",
				message: "Error during account role restore",
				traceId: req.traceId,
				details: {
					error: error.message,
					stack: error.stack,
				},
				metadata: {
					createdAt: new Date(),
					createdBy: adminAccount._id,
					createdByTerminal: req.terminalId,
				},
			});
		}

		res.status(500).json({
			status: "internal-error",
		});
	} finally {
		await session.endSession();
	}
};

export default handler;
